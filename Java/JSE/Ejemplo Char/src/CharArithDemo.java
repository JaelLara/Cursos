// Las variables de caracteres se pueden procesar como enteros
public class CharArithDemo {
	public static void main(String[] args) {
		char ch;
		
		ch = 'X';
		
		System.out.println("ch contiene " + ch);
		
		ch++; //incremento ch
		
		System.out.println("ch ahora es " + ch);
		
		ch = 90;
		System.out.println("ch ahora es " + ch);
	}

}
