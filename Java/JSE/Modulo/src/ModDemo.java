// El operador %
public class ModDemo {
	public static void main(String[] args) {
		int iresult, irem;
		double dresult, drem;
		
		iresult = 10 / 3;
		irem = 10 % 3;
		
		dresult = 10.3 / 3.0;
		drem = 10.3 % 3.0;
		
		System.out.println("Resultado y resto de 10 / 3: " + iresult + " " + irem);
		
		System.out.println("Resultado y resto de 10.3 / 3.0: " + dresult + " " + drem);
	}

}
