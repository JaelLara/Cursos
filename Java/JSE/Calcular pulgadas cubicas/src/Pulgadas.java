/*
 * Calcular el número de pulgadas cúbicas en una milla cúbica
 */
public class Pulgadas {
	public static void main(String[] args) {
		long ci;
		long im;
		
		im = 5820 * 12;
		
		ci = im * im * im;
		
		System.out.println("Estas son " + ci +" las pulgadas cúbicas en una milla cúbica" );
	}

}
