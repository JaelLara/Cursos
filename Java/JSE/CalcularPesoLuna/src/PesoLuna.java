/*
 * Calculo del peso en la Luna
 */
public class PesoLuna {
	public static void main(String[] args) {
		double pesoLuna, pesoTierra;
		
		pesoTierra = 92.6;
		
		pesoLuna = pesoTierra * 17 / 100;
		
		System.out.println("Su peso en al tierra es : " + pesoTierra +" kg y su peso en la Luna seria de " + pesoLuna + "kg.");

	}

}
